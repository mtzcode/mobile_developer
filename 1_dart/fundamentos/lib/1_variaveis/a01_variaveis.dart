void main() {
  int idade = 30; //Tipo Inteiro
  double altura = 1.80; //Tipo Float
  String nome = "Carlos"; //Tipo Texto
  bool estudante = false; //Tipo boobleano (true ou false)

  print("Nome: $nome");
  print("Idade: $idade");
  print("Altura: $altura");
  print("Estudante: $estudante");
}

//- Variável é um espaço nomeado para guardar um valor.
//- Pode ter vários tipos: `int`, `double`, `String`, `bool`.
//- Pode usar `var` (tipo automático), `final` (valor fixo), `const` (valor fixo conhecido antes).
//- Sempre escolha nomes descritivos e use camelCase.