// 3.Use final e const para criar:
// Um valor fixo de PI (3.14)
// Um nome de cidade que não muda durante o programa

void main() {
  const pi = 3.14159;
  final nomeCidade = "Ribeirão Preto";

  print("Qual o valor de PI? Resposta: ${pi.toStringAsFixed(2)}");
  print("Nome da sua cidade: $nomeCidade");
}
